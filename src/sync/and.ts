/**
 * Sync pipeable which takes N number of sync pipeables and returns either `Ok` returned by the last pipeable or `Err` returned by any of the pipeables.
 *
 * @example
 * // PipeableSync<number, Result<string, Error<"FILTER", number> | Error<"CUSTOM", number>>>
 * const customPipe = pipe(
 *   filter((v: number) => !(v % 2)),
 *   wrap(
 *     map((v) => `${v}`),
 *     error("CUSTOM")
 *   ),
 * );
 **/
export function and(...pipeables: PipeableSync[]) {
  const length = pipeables.length;

  return (value: unknown) => {
    let latest = value;

    for (let i = 0; i < length; ++i) {
      const result = pipeables[i](latest);

      if (isErr(result)) {
        return result;
      }

      latest = getOk(result);
    }

    return ok(latest);
  };
}

export const andSync = and;
