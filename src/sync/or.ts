/**
 * Sync pipeable which takes N number of sync pipeables and returns either `Ok` returned by any of the pipeables or `Err` if none of the pipeables returned `Ok`.
 *
 * @example
 * // PipeableSync<number, Result<number, Error<"OR", number>>>
 * const customPipe = or(
 *   filter((v: number) => !(v % 2)),
 *   filter((v: number) => !(v % 3)),
 * );
 **/
export function or(...pipeables: PipeableSync[]) {
  const length = pipeables.length;

  return (value: unknown) => {
    for (let i = 0; i < length; ++i) {
      const result = pipeables[i](value);

      if (isOk(result)) {
        return result;
      }
    }

    return err(error("OR")(value));
  };
}

export const orSync = or;
